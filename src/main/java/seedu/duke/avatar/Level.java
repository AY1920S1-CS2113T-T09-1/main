package seedu.duke.avatar;

/**
 * Class representing the level of the
 * avatar.
 */
public class Level {
    /**
     * Integer level of avatar.
     */
    private int level;

    /**
     * Default initializer for level.
     */
    public Level() {
        int level = 1;
    }

    /**
     * Initializes level with a value.
     */
    public Level(int level) {
        this.level = level;
    }

}
